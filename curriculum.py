def get_subjects(grade: str, field: str):
    return CURRICULUM.get(grade, {}).get(field, [])


def get_weight_for_subject(subject_name: str, target_major: str) -> float:
    if not target_major:
        return 1.0
    for major_key, weight_map in TARGET_MAJOR_WEIGHTS.items():
        if major_key in target_major:
            for subj_key, w in weight_map.items():
                if subj_key in subject_name:
                    return w
    return 1.0
